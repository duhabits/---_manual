#include <iostream>  // Подключаем библиотеку для ввода/вывода
using namespace std; // Используем пространство имен std

// Основная структура программы на C++
int main()
{ // Главная функция, с которой начинается выполнение программы

    // ========== ОСНОВНЫЕ ТИПЫ ДАННЫХ ==========
    int integerVar = 10;        // Целое число (4 байта)
    float floatVar = 3.14f;     // Число с плавающей точкой (4 байта)
    double doubleVar = 3.14159; // Число с двойной точностью (8 байт)
    char charVar = 'A';         // Символ (1 байт)
    bool boolVar = true;        // Логический тип (true/false)
    string stringVar = "Hello"; // Строка (из библиотеки <string>)

    // ========== ВВОД И ВЫВОД ==========
    cout << "Введите число: "; // Вывод текста
    cin >> integerVar;         // Ввод числа с клавиатуры

    cout << "Вы ввели: " << integerVar << endl; // endl - перенос строки

    // ========== ОПЕРАТОРЫ ==========
    int a = 10, b = 5;

    // Арифметические операторы
    int sum = a + b;  // Сложение
    int diff = a - b; // Вычитание
    int mult = a * b; // Умножение
    int div = a / b;  // Деление
    int mod = a % b;  // Остаток от деления

    // Операторы сравнения
    bool isEqual = (a == b);  // Равно
    bool notEqual = (a != b); // Не равно
    bool greater = (a > b);   // Больше
    bool less = (a < b);      // Меньше

    // Логические операторы
    bool andResult = (a > 0 && b > 0); // И
    bool orResult = (a > 0 || b > 0);  // ИЛИ
    bool notResult = !(a > 0);         // НЕ

    return 0; // Завершение программы
}